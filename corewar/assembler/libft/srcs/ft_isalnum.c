int	ft_isalnum(int c)
{
	if (c >= (int)('a') && c <= (int)('z'))
		return (1);
	if (c >= (int)('A') && c <= (int)('Z'))
		return (1);
	if (c >= (int)('0') && c <= (int)('9'))
		return (1);
	return (0);
}
