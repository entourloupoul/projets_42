#include <string.h>

char	*ft_strchr(const char *str, int a)
{
	char	c;
	int		i;

	i = 0;
	c = (char)a;
	while (str[i] != '\0' && str[i] != c)
		i++;
	if (str[i] == '\0' && a != 0)
		return (NULL);
	return (((char *)str) + i);
}
