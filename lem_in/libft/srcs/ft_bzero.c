#include <string.h>

void	ft_bzero(void *s, size_t len)
{
	unsigned char	*data;
	unsigned int	i;

	data = (unsigned char *)s;
	i = 0;
	while (i < len)
	{
		data[i] = '\0';
		i++;
	}
}
