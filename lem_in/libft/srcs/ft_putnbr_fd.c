#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	char	c;
	int		pow;

	if (nb == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		pow = 1;
		if (nb < 0)
		{
			pow = -1;
			write(fd, "-", 1);
		}
		while (nb / pow >= 10)
			pow *= 10;
		while (pow != 0)
		{
			c = nb / pow + 48;
			write(fd, &c, 1);
			nb = nb % pow;
			pow /= 10;
		}
	}
}
